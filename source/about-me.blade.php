                            I just did this to make my friends laugh and constantly horse around with #REFOGANG. 'Refo' is a Dutch abbreviation of 'Reformed' which stands for 'Christian Reformed'.
                            It is youth slang to indicate that some place or someone is very strictly christian. When in 2022 I did a serious attempt at Uptempo, I needed a name.
                            I told my friend "I need something with Refo", on which he immediately replied: "REFOCORE", and that was it! REFOCORE was born.
                        </p>
                        <p>
                            Again it started as a joke, but I became more serious about religion too.
                            I wanted to dive in to christianity because I needed an answer on the question why sh*t's f*cked today, and why everyone is so retarded.
