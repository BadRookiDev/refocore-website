                        <p>
                            My oldest brother (an old hip-hop producer who has the insanely rare skills needed to scratch records) introduced me to ACID pro 7, and thus to producing.
                            That is the sh*ttiest DAW to ever walk the earth, but hey, he said it was the closest thing to logic at the time, and I had no macbook.
                            So I started making House, then Dubstep, then Trap, then House again, then Hardstyle, then Hardcore & Uptempo. And I came to the conclusion that I love making music, whatever it is, but I hate social media marketing, and I suck at mix & mastering.
                            Thus, I accepted the fact that I would never be a famous DJ. But I still wanted to do something useful with music. So I created my own sample pack platform.
                            Software engineering school is no joke however, and they left me with very little time to manage that, so it didn't really grow yet.
                        </p>
