                            Anyway, the point is that admittedly, this name started out a bit as unintended satire of christians, I no longer use this name for that reason.
                            In fact, I'd rather be the change in the Uptempo culture that it needs. I am drug free, I don't drink, I don't even smoke! And despite all that boring-ness I still have fun being sober at Uptempo parties.
